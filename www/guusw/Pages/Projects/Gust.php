<p>This is a game me and 4 other people made for the Brains Eden game jam 2015. In the game you control the wind to maneuver a paper plane flying though a cylindrical world.</p>
<p>Most of my work for this game was graphics/shader related. I did things such as creating and tweaking the lighting inside the cylindrical level, creating a depth fog shader, and creating particles and some UI for the game.
<p>Because we aimed for a smartphone/tablet game we had to make sure it ran at reasonable speeds, this was the first time I researched and did some optimization for mobile devices in unity.</p>
<p>We released the game on the Google Play store for free.</p>
