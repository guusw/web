<p>
In my last year of university I did an internship at <a href="https://www.siliconstudio.co.jp/en/">Silicon Studio</a> working on <a href="https://xenko.com/">Xenko</a>, an open-source C# game engine.
</p>

<p>
The first major feature I worked on was the navigation mesh integration into both the game engine and the editor.
This feature can be seen in action <a href="https://xenko.com/blog/navigation-mesh/">here</a> or in the video below which shows the engine's top-down RPG template using this navigation mesh.
</p>

<p>
The second video shows the light shafts feature in the engine that allows you to create volumetric lighting effects using shadow maps.
I helped to complete this feature and it is now available in the released version of the engine.
</p>

<p>
During this project I learned a lot about the inner workings of a game engine and it's tools.
</p>

